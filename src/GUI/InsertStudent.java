    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String name = txt_name.getText();
        String rollno = txt_roll.getText();
        String gender = (String)combo_gender.getSelectedItem();
        String fatherName=txt_fathername.getText();
        String course=(String)combo_course.getSelectedItem();
        String branch =(String)combo_branch.getSelectedItem();
        
        try {
            Connection con =ConnectionProviderCode.getcon();
            Statement st = con.createStatement();
            st.executeUpdate("INSERT INTO student VALUES('"+name +"','" +rollno +"','"+gender+"','"+fatherName +"','"+course +"','" + branch +"')");
            JOptionPane.showConfirmDialog(null, "Insertion Successful");
            setVisible(false);
            InsertStudent frame =new InsertStudent();
            frame.setVisible(true);
            frame.setLocationRelativeTo(null);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

