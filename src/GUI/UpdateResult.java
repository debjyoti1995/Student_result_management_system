    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String rollNo =jTextField1.getText().trim();
        try {
            Connection con=ConnectionProviderCode.getcon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("SELECT * FROM result WHERE rollNo='"+rollNo+"'");
        
            if(!rs.first()){
                JOptionPane.showMessageDialog(null, "Entered RollNo.doesn't exist");
            }
            else{
            jTextField1.setEditable(false);
            txt_phy.setText(rs.getString(2));
            txt_math.setText(rs.getString(3));
            txt_chem.setText(rs.getString(4));
            txt_elec.setText(rs.getString(5));
            txt_bio.setText(rs.getString(6));
            
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

